# Operadores matemáticas python:
print(2 + 3)  # soma
print(10 - 5)  # subtração
print(10 * 2)  # Multiplicação
print(2**3)  # potencia
print(5 / 2)  # Divisão real
print(5 // 2)  # divisão inteira
print(7 % 3)  # mod resto de divisão

# Operadores relacionais e lógicos:

# Boolens:
